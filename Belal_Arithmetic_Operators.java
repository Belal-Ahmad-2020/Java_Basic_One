public class Belal_Arithmetic_Operators {
    public static void main(String[] args) {
        // Arithmetic Operators
        int num1, num2;
        float result;
        // Addition 
        num1 = 4;
        num2 = 3;
        result = 4+3;
        System.out.println(result);
        
        //Subtraction 
        num1 = 4;
        num2 = 3;
        result = 4-3;
        
        // Multiplication
        num1 = 4;
        num2 = 3;
        result = 4*3;
        System.out.println(result);
        
           // Division
        num1 = 4;
        num2 = 3;
        result =  (float) (4/3);
        System.out.println(result);
        
           // Modulus
        num1 = 4;
        num2 = 3;
        result = 4%3;
        System.out.println(result);
        
        
        // Increment and Decrement        
        num1++;
        num2--;
        System.out.println(num1);
        
        
        System.out.println(num2);
        
        
        
    }
    
    
    
    
    
}
