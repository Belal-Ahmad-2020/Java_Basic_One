public class Belal_Boolean {
    public static void main(String[] args) {
        // in programming, you will need a data type that can have only two values 
        
        boolean is_electricity_problem;
        is_electricity_problem = true;
        System.out.println(is_electricity_problem);
        
        is_electricity_problem = false;
        System.out.println(is_electricity_problem);
        
        // expression 
        int x, y;        
        x=5;
        y=8;
        
        // check if x is greater than y
        System.out.println(x>y);
        
        // check if x is less than y
        System.out.println(x<y);
        
        // check if x is equal to y
        System.out.println(x == y);
        
        
        
    }
}
