public class Variables {
    public static void main(String[] args) {
        /*
        variables are used to store data.
        there are different types of variables in java
        String  -- is used to store or text values
        int     -- is used to storedecimal numbers 
        float   -- is used to store floating point numbers
        char    -- is used to store characters
        boolean -- is used to store boolean values
        */
        
        String name = "Ahmad";
        int number = 22;
        float num = 22.5f;
        char letter = 'a';
        boolean is_married = false;
        
        // declaring many variables at a time 
        int a=3, b=5, c=9;
        
        
        
        System.out.println(name);
        System.out.println(number);
        System.out.println(num);
        System.out.println(letter);
        System.out.println(is_married);
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        
        
        
        
    }
}
